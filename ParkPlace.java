public class ParkPlace {
	private int size;
	private Vehicle vehicle;
	//ParkPlace Class' constructor.
	public ParkPlace(Vehicle vehicle){
		this.vehicle = vehicle;
	}
	// This method get parkplace's size.
	public int getSize(){
		return size;
	}
	// This method get vehicle in the park place.
	public Vehicle getVehicle(){
		return vehicle;
	}


}
